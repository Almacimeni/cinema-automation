    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int selectedRowindex = tabloFilm.getSelectedRow();
        String filmAdi = (String) tabloFilm.getValueAt(selectedRowindex, 0);
        String filmSalon = (String) tabloFilm.getValueAt(selectedRowindex, 1);
        String filmSeans = (String) tabloFilm.getValueAt(selectedRowindex, 2);
        
        try {
            aa.filmSil(filmAdi, filmSalon, filmSeans);
            aa.listele();
            JOptionPane.showMessageDialog(rootPane, "Silindi...");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(rootPane, "Hatabu: "+ ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

